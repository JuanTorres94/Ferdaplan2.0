package is.vidmot.switcher;

/**
 * Enum sem skilgreinir viðmótstré (views) í forritinu.
 * ADAL er aðalviðmótið og FERD er viðmótið sem sýnir eina ferð.
 */
public enum View {
    /** Aðalviðmót með lista af ferðum. */
    ADAL,
    /** Viðmót sem sýnir nánari upplýsingar um eina ferð. */
    FERD
}